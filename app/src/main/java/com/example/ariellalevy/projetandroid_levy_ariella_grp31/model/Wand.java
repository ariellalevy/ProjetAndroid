package com.example.ariellalevy.projetandroid_levy_ariella_grp31.model;

public class Wand {
    String wood;
    String core;
    int length;
    public String getWood() {
        return wood;
    }

    public void setWood(String wood) {
        this.wood = wood;
    }

    public String getCore() {
        return core;
    }

    public void setCore(String core) {
        this.core = core;
    }

    public int getLength() {
        return length;
    }

    public void setLength(int length) {
        this.length = length;
    }

}
